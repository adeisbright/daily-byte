
def peakIndexInMountainArray(arr , index = 0):

    """
    :type arr: List[int]
    :rtype: int
    """
    lower = arr[0] 
    remnant = arr[1:] 
    mid = remnant[0]
    upper = remnant[1]
    index += 1
    if lower < mid and mid > upper :
        return index
    
    return peakIndexInMountainArray(remnant , index)

